class WeatherModel {
  final String icon;
  final int time;
  final double temperature;
  final String description;
  final int humidity;
  final int moisture;
  final int light;

  WeatherModel({
    required this.icon,
    required this.time,
    required this.temperature,
    required this.description,
    required this.humidity,
    required this.moisture,
    required this.light,
  });
}
